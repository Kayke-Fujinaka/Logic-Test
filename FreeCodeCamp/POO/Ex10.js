// Função anônima
// Ela executa logo após ser declarada.
// Note que a função não possui nome e não é armazenada em uma variável.
// Os dois parênteses () ao final da expressão da função faz com que ela seja imediatamente executada ou invocada.
(function () {
  console.log("I'm Annonymus");
})();

// Função não anônima
function makeNest() {
  console.log("I'm Frouxo");
}

makeNest();
